2- C# Nedir?
Orta seviyeli bir programlama dilidir. Orta seviyeden kastımız kodun okunabilirliği, anlaşılabilirliğidir.
Microsoft tarafından geliştirilmiş, açık kaynak kodludur. Açık kaynak olması, herkes tarafından gelişime imkan sunulmasını sağlar. Kodlara herkes ulaşabilir. 
Derlenebilir bir programlama dilidr ve oop yi destekler. Oop , nesneler ve sınıflar arasındaki ilişkiyi açıklayan bir kavramı ifade eder ve nesne tabanlı yaklaşımı ifade eder. 
.Net çatısını kullanır. 
C# ile, web , masaüstü uygulaması, mobil, erp, oyun programlama, veri bilimi, aklınıza ne geliyorsa programlayabilirsiniz. 
Yeni bir programlama dili beşeri seviyede algılanabiliyorsa ve kodları metinselse bu yüksek seviyede bir dildir. Ama dil okunabilirliği düşüyorsa sembollerle ifade edilip matematiğe/mantığa göre yorumlanıyorsa o dil düşük seviyededir. 
C benzeri bir dildir. C'nin bir üst versiyonu C++'dır. C++'ın bir üst versiyonu ise C++++ (2 ++'nın yan yan gelemsi #) C#'dır.Buradaki ++ aslında +1 anlamına gelir. 

3- .Net Framework ve .Net Core Nedir? Farkları Nelerdir? 
.Net nedir, microsoftun geliştiriciler için sunnduğu, geliştiricilerin kullanabileceği teknolojileri ifade eder. Bu teknolojiler, kütüphane, ide, framework, programlama dilleri, mimariler... Ama işletim sistemi .net çatısı altına girmez çünkü son kullanıcılar için sunulmuştur, geliştiriciler için değil. 
Programlama dilinde mimari, bir projenin nasıl organize edildiği, düzenlenip ayrıldığını, yapılandırıldığını ifade eder. 
Biz gerçek hayatta uygulama yazarken aslında yazdığımız uygulama değildir, bir çözümdür. 
.net framework ve .net core ifade etmede, yazılım geliştirme platformları, çerçeveleri şeklinde söylemde bulunulabilir. 






